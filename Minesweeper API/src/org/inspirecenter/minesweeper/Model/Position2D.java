package org.inspirecenter.minesweeper.Model;

public class Position2D {

    private final int x;
    private final int y;

    public Position2D(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

}
